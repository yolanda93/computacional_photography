R=[1,2,3];
G=[1,2,3];
B=[1,2,3];

RGB=[R(:)';G(:)';B(:)'];
M1=[0.6844,0.1651,0.1009;0.3600,0.7634,-0.1235;0.0389,-0.0575,1.1072];
M2=[3.2406,-1.5372,-0.4986;-0.9689,1.8758,0.0415;0.0557,-0.2040,1.0570];
M=M1*M2;
sRGB=M*RGB;

for i=1:length(sRGB(:))
   if(sRGB(i)<0)
       sRGB(i)=0;
   end
   if(sRGB(i)>1)
       sRGB(i)=1;
   end
end